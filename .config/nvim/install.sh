#!/bin/sh

install() {
    mv ~/.config/nvim ~/.config/nvim.old
    rm -rf ~/.local/share/nvim
    rm -rf ~/.local/state/nvim
    mkdir ~/.config/nvim
    cp -r ./* ~/.config/nvim
    nvim
}

